        //Fields
        private List<Rectangle> drawing; //Little drawing squares
        private bool pencil; //Bool to see whether to draw or erase

        public NoteBook(Texture2D testxture, Texture2D[] textureArray)
            : base(testxture, textureArray)
        {
            base.Rectangles = new Rectangle[]{
            new Rectangle(1487,757,148,500), //Pencil
            new Rectangle(1652,757,249,500), //Eraser
            new Rectangle(57,150,1406,789)}; //Notebook
            SetNotebook();
            pencil = true;
        }

        //Methods
        public override void Draw(SpriteBatch sb)
        {
            if (pencil)
            { //Draw pencil up if in pencil mode
                sb.Draw(base.TextureArray[0], new Rectangle(0, 0, 1920, 1080), Color.White);
            }
            else
            { //Draw eraser up if in eraser mode
                sb.Draw(base.TextureArray[1], new Rectangle(0, 0, 1920, 1080), Color.White);
            }
            for (int i = 0; i < drawing.Count; i++)
            { //Draw the drawing squares
                sb.Draw(base.TestTexture, drawing[i], Color.Black);
            }
        }

        public override GameState Update(MouseState mouState, MouseState prevMouState, KeyboardState kbState, KeyboardState prevKbState)
        {
            if (mouState.LeftButton == ButtonState.Pressed)
            { //If clicking
                if (rectCollision().Equals(2))
                { //If on the notebook
                    if (pencil)
                    { //In draw mode add squares
                        drawing.Add(new Rectangle(mouState.X - 10, mouState.Y - 10, 20, 20));
                    }
                    else
                    { //In erase mdoe remove  squares
                        for (int i = 0; i < drawing.Count; i++)
                        {
                            if (drawing[i].Contains(mouState.Position))
                            {
                                drawing.RemoveAt(i);
                                i--;
                            }
                        }
                    }
                }
                else if (rectCollision().Equals(0))
                { //If on the pencil and it's a single click, go pencil mode
                    if (prevMouState.LeftButton == ButtonState.Released)
                    {
                        pencil = true;
                    }
                }
                else if (rectCollision().Equals(1))
                { //If on the eraser and it's a single click, go eraser mode
                    if (prevMouState.LeftButton == ButtonState.Released)
                    {
                        if (!pencil)
                        { //If already on eraser mode, clear the drawing
                            drawing.Clear();
                        }
                        pencil = false;
                    }
                }
            }
            if (kbState.IsKeyDown(Keys.Q) && prevKbState.IsKeyUp(Keys.Q))
            { //Switch to in game on Q
                return GameState.InGame;
            }
            if (kbState.IsKeyDown(Keys.P) && prevKbState.IsKeyUp(Keys.P))
            { //Give notebook log
                System.Diagnostics.Debug.WriteLine("\n\n\ndrawing = new List<Rectangle>{");
                for (int i = 0; i < drawing.Count-1; i++)
                {
                    System.Diagnostics.Debug.WriteLine("                new Rectangle("+drawing[i].X+","
                        +drawing[i].Y+","+drawing[i].Width+","+drawing[i].Height+"),");
                }
                System.Diagnostics.Debug.WriteLine("                new Rectangle(" + drawing[drawing.Count - 1].X + ","
                        + drawing[drawing.Count - 1].Y + "," + drawing[drawing.Count - 1].Width + "," + drawing[drawing.Count - 1].Height + ")");
                System.Diagnostics.Debug.WriteLine("                };\n\n\n");
            }
            return GameState.Notebook;
        }
